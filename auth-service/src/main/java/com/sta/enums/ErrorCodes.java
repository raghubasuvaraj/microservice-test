package com.sta.enums;

public enum ErrorCodes {

	// Informational responses (100–199)
	CONSTRAINT_VIOLATION(101),

	// Successful responses (200–299)
	OK(200),

	// Client error responses (400–499)
	BAD_REQUEST(400), UNAUTHORIZED(401), PAYMENT_REQUIRED(402), FORBIDDEN(403), NOT_FOUND(404), METHOD_NOT_ALLOWED(405),
	NOT_ACCEPTABLE(406), PROXY_AUTHENTICATION_REQUIRED(407), REQUEST_TIMEOUT(408), CONFLICT(409), GONE(410),
	LENGTH_REQUIRED(411), PRECONDITION_FAILED(412), PAYLOAD_TOO_LARGE(413), REQUEST_URI_TOO_LONG(414),
	UNSUPPORTED_MEDIA_TYPE(415), REQUESTED_RANGE_NOT_SATISFIABLE(416), EXPECTATION_FAILED(417), I_AM_A_TEAPOT(418),
	MISDIRECTED_REQUEST(421), UNPROCESSABLE_ENTITY(422), LOCKED(423), FAILED_DEPENDENCY(424), UPGRADE_REQUIRED(426),
	PRECONDITION_REQUIRED(428), TOO_MANY_REQUESTS(429), REQUEST_HEADER_FIELDS_TOO_LARGE(431),
	CONNECTION_CLOSED_WITHOUT_RESPONSE(444), UNAVAILABLE_FOR_LEGAL_REASONS(451), CLIENT_CLOSED_REQUEST(499),

	// Server error responses (500–599)
	INTERNAL_SERVER_ERROR(500);

	private int code;

	ErrorCodes(int code) {
		this.code = code;
	}

	public int getCode() {
		return code;
	}
}
